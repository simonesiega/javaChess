package utils.constant;

/**
 * Classe che definisce i colori degli scacchi come costanti.
 * Viene utilizzata per rappresentare i colori dei pezzi sulla scacchiera.
 */
public class ChessColor {

        /** Rappresenta il colore bianco (0) */
        public static final int WHITE = 0;

        /** Rappresenta il colore nero (1) */
        public static final int BLACK = 1;

        /** Rappresenta una casella vuota (-1), usata per indicare una casella senza pezzo */
        public static final int BLANK = -1;
}

